package com.lgh.library.model;

public class Main1 {


    private String id;
    private String count;
    private String name;

    public Main1(String id, String count, String name) {
        this.id = id;
        this.count = count;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}


